const ITEM_PIXELS = 48;
const SLOT_COUNT = 10;
const ABILITY_PIXELS = 36;
// Abilities have three upgrade tiers; we render one segment per tier.
const ABILITY_TIERS = 3;

export function PlayerDetail({
  player,
  stats,
  items,
  abilities,
  abilityLevels,
  onBack,
}: {
  player: PlayerInfo;
  stats: PlayerPosition | undefined;
  items: HeroItems | undefined;
  abilities: AbilitySlot[] | undefined;
  abilityLevels: Map<number, number> | undefined;
  onBack: () => void;
}) {
  const portrait = heroPortraitUrl(player.hero_id);
  const teamLabel = TEAM_NAMES[player.team] ?? `Team ${player.team}`;
  const teamColor = TEAM_COLORS[player.team] ?? "#888";

  // Pad to a fixed 10-slot inventory so empty slots stay visible.
  const slots = (items?.items ?? []).slice(0, SLOT_COUNT);
  while (slots.length < SLOT_COUNT) {
    slots.push({ ability_id: 0, ability_name: "" });
  }

  // Souls invested per category in the current inventory (sum of item costs).
  const investment = React.useMemo(() => {
    const t: Record<ItemCategory, number> = {
      weapon: 0,
      vitality: 0,
      spirit: 0,
    };
    for (const it of items?.items ?? []) {
      const stat = ITEM_STATS[it.ability_name];
      if (stat) t[stat.cat] += stat.cost;
    }
    return t;
  }, [items]);

  return (
    <div className="flex w-80 min-h-0 flex-shrink-0 flex-col gap-3 sm:w-96">
      <div className="flex items-center gap-2">
        <button
          type="button"
          onClick={onBack}
          aria-label="Back to teams"
          className="flex size-7 flex-shrink-0 items-center justify-center rounded-md border border-border bg-card text-foreground transition-colors hover:bg-accent"
        >
          <ChevronLeft className="size-4" />
        </button>
        {portrait ? (
          <img
            src={portrait}
            alt={player.hero_name}
            width={32}
            height={32}
            className="size-8 flex-shrink-0 rounded object-contain"
          />
        ) : (
          <div className="size-8 flex-shrink-0 rounded bg-muted" />
        )}
        <div className="min-w-0 flex-1">
          <p className="truncate text-sm font-semibold leading-tight">
            {player.hero_name || "—"}
          </p>
          <p className="truncate text-xs text-muted-foreground leading-tight">
            {player.name || "—"}
          </p>
        </div>
        <span
          className="text-xs font-semibold uppercase tracking-wide"
          style={{ color: teamColor }}
        >
          {teamLabel}
        </span>
      </div>

      <CollapsibleSection
        title="Health"
        right={
          <span className="text-xs font-medium tabular-nums">
            <span className="text-foreground">{stats?.health ?? 0}</span>
            <span className="text-muted-foreground">
              {" / "}
              {stats?.max_health ?? 0}
            </span>
          </span>
        }
      >
        <HealthBar
          health={stats?.health ?? 0}
          maxHealth={stats?.max_health ?? 0}
        />
      </CollapsibleSection>

      {abilities && abilities.length > 0 && (
        <CollapsibleSection title="Abilities">
          <div className="flex gap-2">
            {abilities.map((a) => (
              <div
                key={a.ability_id}
                className="flex min-w-0 flex-1 flex-col items-center gap-1.5"
              >
                <div className="flex aspect-square w-full items-center justify-center rounded-md border border-border bg-muted/20">
                  <AbilityIcon name={a.ability_name} size={ABILITY_PIXELS} />
                </div>
                <LevelBars
                  level={abilityLevels?.get(a.ability_id) ?? 0}
                  color={teamColor}
                />
              </div>
            ))}
          </div>
        </CollapsibleSection>
      )}

      <CollapsibleSection
        title="Items"
        right={
          <span className="flex items-center gap-2 text-xs font-semibold tabular-nums">
            <span
              style={{ color: CATEGORY_COLORS.weapon }}
              title={`Weapon: ${investment.weapon.toLocaleString()}`}
            >
              {formatSouls(investment.weapon)}
            </span>
            <span
              style={{ color: CATEGORY_COLORS.vitality }}
              title={`Vitality: ${investment.vitality.toLocaleString()}`}
            >
              {formatSouls(investment.vitality)}
            </span>
            <span
              style={{ color: CATEGORY_COLORS.spirit }}
              title={`Spirit: ${investment.spirit.toLocaleString()}`}
            >
              {formatSouls(investment.spirit)}
            </span>
          </span>
        }
      >
        <div className="grid grid-cols-5 gap-2">
          {slots.map((it, i) =>
            it.ability_id ? (
              (() => {
                // Frame each item in its category color (gun/vitality/spirit);
                // border-box keeps the 48px footprint aligned with empty slots.
                const cat = ITEM_STATS[it.ability_name]?.cat;
                const color = cat ? CATEGORY_COLORS[cat] : undefined;
                return (
                  <ItemIcon
                    key={`${i}-${it.ability_id}`}
                    abilityId={it.ability_id}
                    abilityName={it.ability_name}
                    size={ITEM_PIXELS}
                    className={cn("border-2", !color && "border-border")}
                    style={color ? { borderColor: color } : undefined}
                  />
                );
              })()
            ) : (
              <div
                key={`empty-${i}`}
                style={{ width: ITEM_PIXELS, height: ITEM_PIXELS }}
                className="rounded border border-border bg-muted/30"
              />
            ),
          )}
        </div>
      </CollapsibleSection>

      <CollapsibleSection title="Bonuses">
        <dl className="grid grid-cols-3 gap-x-3 gap-y-2">
          <Bonus
            icon={HealthIcon}
            label="Bonus HP"
            value={stats?.bonus_health ?? 0}
          />
          <Bonus
            icon={WeaponDamageIcon}
            label="Weapon DMG"
            value={stats?.weapon_damage ?? 0}
            percent
          />
          <Bonus
            icon={SpiritIcon}
            label="Spirit"
            value={stats?.spirit_power ?? 0}
          />
          <Bonus
            icon={FireRateIcon}
            label="Fire Rate"
            value={stats?.fire_rate ?? 0}
            percent
          />
          <Bonus
            icon={CooldownIcon}
            label="CDR"
            value={stats?.cooldown_reduction ?? 0}
            percent
          />
          <Bonus
            icon={AmmoIcon}
            label="Ammo"
            value={stats?.ammo ?? 0}
            percent
          />
        </dl>
      </CollapsibleSection>
    </div>
  );
}

// A bordered card whose body collapses when the header is clicked. `right` is
// optional trailing header content (e.g. the health value or item totals).
function CollapsibleSection({
  title,
  right,
  children,
}: {
  title: string;
  right?: React.ReactNode;
  children: React.ReactNode;
}) {
  const [open, setOpen] = React.useState(true);
  return (
    <section className="rounded-md border border-border bg-card p-3">
      <button
        type="button"
        onClick={() => setOpen((o) => !o)}
        aria-expanded={open}
        className="flex w-full items-center justify-between gap-2 text-left"
      >
        <span className="flex items-center gap-1 text-xs font-medium uppercase tracking-wide text-muted-foreground">
          <ChevronDown
            className={cn(
              "size-3.5 shrink-0 transition-transform",
              !open && "-rotate-90",
            )}
            aria-hidden
          />
          {title}
        </span>
        {right}
      </button>
      {open && <div className="mt-2">{children}</div>}
    </section>
  );
}

function HealthBar({
  health,
  maxHealth,
}: {
  health: number;
  maxHealth: number;
}) {
  const pct =
    maxHealth > 0
      ? Math.max(0, Math.min(1, health / maxHealth)) * 100
      : 0;
  // Hue shifts from green (full) to red (empty) — quick visual scan in
  // addition to the explicit numbers above.
  const hue = (pct / 100) * 120;
  const fill = pct > 0 ? `hsl(${hue} 70% 45%)` : "transparent";
  return (
    <div className="h-2 w-full overflow-hidden rounded-full bg-muted">
      <div
        className="h-full rounded-full transition-all"
        style={{ width: `${pct}%`, backgroundColor: fill }}
      />
    </div>
  );
}

// Three horizontally-stacked segments, `level` of them filled, showing how many
// upgrade tiers are spent on an ability (0 = unlocked / no tiers, up to 3).
function LevelBars({ level, color }: { level: number; color: string }) {
  return (
    <div
      className="flex w-full gap-0.5"
      title={level > 0 ? `Tier ${Math.min(level, ABILITY_TIERS)}` : "Unlocked"}
    >
      {Array.from({ length: ABILITY_TIERS }, (_, i) => (
        <span
          key={i}
          className={cn(
            "h-1.5 flex-1 rounded-sm",
            i < level ? "" : "bg-muted",
          )}
          style={i < level ? { backgroundColor: color } : undefined}
        />
      ))}
    </div>
  );
}

function Bonus({
  icon: Icon,
  label,
  value,
  percent,
}: {
  icon: React.ComponentType<{ className?: string }>;
  label: string;
  value: number;
  percent?: boolean;
}) {
  return (
    <div>
      <dt className="flex items-center gap-1 text-[10px] uppercase tracking-wide text-muted-foreground">
        <Icon className="size-3" />
        {label}
      </dt>
      <dd className="text-sm font-medium tabular-nums">
        {formatBonus(value)}
        {percent ? "%" : ""}
      </dd>
    </div>
  );
}

function formatBonus(value: number): string {
  // Stat-modifier values are floats; small magnitudes round nicely with 1
  // decimal, larger values look better as integers.
  if (value === 0) return "0";
  const abs = Math.abs(value);
  const rounded =
    abs >= 100 ? Math.round(value) : Math.round(value * 10) / 10;
  return rounded > 0 ? `+${rounded}` : `${rounded}`;
}
